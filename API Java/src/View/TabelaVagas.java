private void listarVagas(){
    try{
        VagaDAO objvagadao = new VagaDAO();
        DefaultTableModel model = (DefaultTableModel) tblvaga.getModel();
        model.setNumRows(0);
        
        ArrayList<Vaga> lista = objvagadao.PesquisarVagas();
        
        for (int num = 0; num < lista.size(); num++){
            model.addRow(new Object[]{
               
                lista.get(num).getId_vaga(),
                lista.get(num).getNome(),
                lista.get(num).getDescricao(),
                lista.get(num).getSalario(),
                lista.get(num).getCargo(),
                lista.get(num).getExpe_prof()
            });
        }
        
    } catch(Exception erro){
        JOptionPane.showMessageDialog(null, "Listar Vagas" + erro);
    }
}

private void alterarVaga(){
    int id_vaga;
    double Salario;
    String Nome, Descricao, Cargo, Expe_prof;
    
    id_vaga = Integer.parseInt(txtID.getText());
    Salario = Integer.parseInt(txtPrenSal.getText());
    Nome = txtVaga.getText();
    Descricao = txtDesc.getText();
    Cargo = txtCargo.getText();
    Expe_prof = txtExp.getText();
    
    Vaga objvaga = new Vaga();
    
    objvaga.setId_vaga(id_vaga);
    objvaga.setDescricao(Descricao);
    objvaga.setSalario(Salario);
    objvaga.setNome(Nome);
    objvaga.setCargo(Cargo);
    objvaga.setExpe_prof(Expe_prof);
    
    VagaDAO objvagadao = new VagaDAO();
    objvagadao.alterarVaga(objvaga);
    }

private void listfiltrarVagas(String nome, String cargo){
    try{
        VagaDAO objvagadao = new VagaDAO();
        DefaultTableModel model = (DefaultTableModel) tblvaga.getModel();
        model.setNumRows(0);
        
        ArrayList<Vaga> lista = objvagadao.FiltrarVaga(nome, cargo);
        
        for (int num = 0; num < lista.size(); num++){
            model.addRow(new Object[]{
               
                lista.get(num).getId_vaga(),
                lista.get(num).getNome(),
                lista.get(num).getDescricao(),
                lista.get(num).getSalario(),
                lista.get(num).getCargo(),
                lista.get(num).getExpe_prof()
            });
        }
        
    } catch(Exception erro){
        JOptionPane.showMessageDialog(null, "Listar Vagas" + erro);
    }
}

